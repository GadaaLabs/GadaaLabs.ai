// ─── Main component ───────────────────────────────────────────────────────────

export interface DataScienceAgentProps {
  summary: DatasetSummary;
  summaryText: string;
  /** Called when all 7 phases complete successfully */
  onComplete?: () => void;
  /** Called with all phase outputs when all 7 phases complete */
  onPhasesComplete?: (outputs: Record<string, string>) => void;
}

export function DataScienceAgent({ summary, summaryText, onComplete, onPhasesComplete }: DataScienceAgentProps) {
  const [phaseStates, setPhaseStates] = useState<Record<PhaseId, PhaseState>>(initialPhaseStates);
  const [running, setRunning] = useState(false);
  const [done, setDone] = useState(false);
  const [currentPhaseIndex, setCurrentPhaseIndex] = useState(-1);
  const [fatalError, setFatalError] = useState<string | null>(null);

  // Ref mirrors state for stale-closure-safe reads inside async callbacks
  const statesRef = useRef<Record<PhaseId, PhaseState>>(initialPhaseStates());
  const abortRef = useRef<AbortController | null>(null);

  // ── Notify parent when done ────────────────────────────────────────────────
  useEffect(() => {
    if (done && onComplete) {
      onComplete();
    }
    if (done && onPhasesComplete) {
      const outputs = Object.fromEntries(
        PHASE_IDS.map(id => [id, statesRef.current[id].output])
      ) as Record<string, string>;
      onPhasesComplete(outputs);
    }
  }, [done, onComplete, onPhasesComplete]);

  // ── State helper ───────────────────────────────────────────────────────────
  const setPhase = useCallback((id: PhaseId, patch: Partial<PhaseState>) => {
    setPhaseStates((prev) => {
      const next = { ...prev, [id]: { ...prev[id], ...patch } };
      statesRef.current = next;
      return next;
    });
  }, []);

  const toggleExpand = useCallback((id: PhaseId) => {
    setPhaseStates((prev) => ({
      ...prev,
      [id]: { ...prev[id], expanded: !prev[id].expanded },
    }));
  }, []);

  // ── Pipeline runner ────────────────────────────────────────────────────────
  const runPipeline = useCallback(async () => {
    if (running) return;

    const fresh = initialPhaseStates();
    setPhaseStates(fresh);
    statesRef.current = fresh;
    setRunning(true);
    setDone(false);
    setFatalError(null);
    setCurrentPhaseIndex(0);

    abortRef.current = new AbortController();

    try {
      for (let i = 0; i < PHASES.length; i++) {
        const phase = PHASES[i];
        setCurrentPhaseIndex(i);

        const context = buildContext(statesRef.current, i);
        setPhase(phase.id, { status: "running", output: "" });

        let accumulated = "";

        try {
          const res = await fetch("/api/ai/data-science-agent", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ summaryText, phase: phase.id, context }),
            signal: abortRef.current.signal,
          });

          if (!res.ok) {
            const errData = await res.json().catch(() => ({ error: `HTTP ${res.status}` }));
            throw new Error((errData as { error?: string }).error ?? `HTTP ${res.status}`);
          }

          if (!res.body) throw new Error("No response stream");

          const reader = res.body.getReader();
          const decoder = new TextDecoder();

          while (true) {
            const { done: streamDone, value } = await reader.read();
            if (streamDone) break;
            accumulated += decoder.decode(value, { stream: true });
            setPhase(phase.id, { output: accumulated });
          }

          setPhase(phase.id, { status: "done", output: accumulated });
        } catch (e) {
          if ((e as Error).name === "AbortError") {
            setPhase(phase.id, { status: "error", output: "Analysis cancelled." });
            return;
          }
          const msg = (e as Error).message ?? "Unknown error";
          setPhase(phase.id, { status: "error", output: `Phase failed: ${msg}` });
          setFatalError(`Phase "${phase.label}" failed: ${msg}`);
          return;
        }
      }

      setDone(true);
    } finally {
      setRunning(false);
      setCurrentPhaseIndex(-1);
    }
  }, [running, summaryText, setPhase]);

  const handleCancel = useCallback(() => {
    abortRef.current?.abort();
  }, []);

  // ── Derived ────────────────────────────────────────────────────────────────
  const completedCount = PHASE_IDS.filter((id) => phaseStates[id].status === "done").length;
  const hasStarted = PHASE_IDS.some((id) => phaseStates[id].status !== "idle");

  // ── Render ─────────────────────────────────────────────────────────────────
  return (
    <div className="space-y-4">

      {/* ── Agent header ── */}
      <div
        className="rounded-xl px-5 py-4 flex flex-wrap items-center justify-between gap-4"
        style={{
          background: "var(--color-bg-surface)",
          border: "1px solid var(--color-border-default)",
        }}
      >
        <div className="flex items-center gap-3">
          <div
            className="flex h-9 w-9 items-center justify-center rounded-xl shrink-0"
            style={{
              background: "linear-gradient(135deg, var(--color-purple-600), var(--color-cyan-500))",
            }}
          >
            <Brain className="h-4 w-4 text-white" />
          </div>
          <div>
            <p className="font-bold text-sm" style={{ color: "var(--color-text-primary)" }}>
              Autonomous Data Science Agent
            </p>
            <p className="text-xs mt-0.5" style={{ color: "var(--color-text-muted)" }}>
              {!hasStarted
                ? "7-phase pipeline — Triage → Stats → Hypotheses → Strategy → Code → Business → Decision"
                : done
                ? "All 7 phases complete"
                : running
                ? `Phase ${currentPhaseIndex + 1} of 7 — ${PHASES[currentPhaseIndex]?.label ?? "…"}`
                : `${completedCount} of 7 phases complete`}
            </p>
          </div>
        </div>

        <div className="flex items-center gap-2">
          {running && (
            <button
              onClick={handleCancel}
              className="inline-flex items-center gap-2 px-3 py-1.5 rounded-lg text-xs font-semibold"
              style={{
                background: "rgba(239,68,68,0.08)",
                border: "1px solid rgba(239,68,68,0.25)",
                color: "var(--color-error)",
              }}
            >
              Cancel
            </button>
          )}
          {!running && (
            <button
              onClick={runPipeline}
              className="inline-flex items-center gap-2 px-5 py-2.5 rounded-xl text-sm font-semibold transition-all hover:-translate-y-0.5"
              style={{
                background: "linear-gradient(135deg, var(--color-purple-600), var(--color-purple-500))",
                color: "#fff",
                boxShadow: "var(--glow-purple-sm)",
              }}
            >
              <Zap className="h-4 w-4" />
              {done ? "Re-run Analysis" : hasStarted ? "Retry" : "Launch 7-Phase Analysis"}
            </button>
          )}
        </div>
      </div>

      {/* ── Phase tracker (appears once started) ── */}
      {hasStarted && (
        <PhaseTracker states={phaseStates} currentIndex={currentPhaseIndex} />
      )}

      {/* ── Fatal error notice ── */}
      {fatalError && (
        <div
          className="rounded-xl px-4 py-3 flex items-start gap-2"
          style={{
            background: "rgba(239,68,68,0.08)",
            border: "1px solid rgba(239,68,68,0.25)",
          }}
        >
          <AlertTriangle
            className="h-4 w-4 mt-0.5 shrink-0"
            style={{ color: "var(--color-error)" }}
          />
          <p className="text-sm" style={{ color: "var(--color-error)" }}>
            {fatalError}
          </p>
        </div>
      )}

      {/* ── Empty state ── */}
      {!hasStarted && (
        <div
          className="rounded-xl p-10 text-center"
          style={{
            background: "var(--color-bg-surface)",
            border: "1px solid var(--color-border-default)",
          }}
        >
          <FlaskConical
            className="h-12 w-12 mx-auto mb-4"
            style={{ color: "var(--color-text-disabled)" }}
          />
          <p className="font-semibold text-sm mb-2" style={{ color: "var(--color-text-secondary)" }}>
            Mission Control — Ready
          </p>
          <p
            className="text-xs max-w-md mx-auto mb-6 leading-relaxed"
            style={{ color: "var(--color-text-muted)" }}
          >
            7 autonomous phases run sequentially — each phase builds on the decisions of the last.
            Triage → Statistical Analysis → Hypothesis Engine → ML Battle Plan →
            Production Code → Business Intelligence → Decision Brief.
          </p>
          <button
            onClick={runPipeline}
            className="inline-flex items-center gap-2 px-6 py-3 rounded-xl text-sm font-semibold"
            style={{
              background: "linear-gradient(135deg, var(--color-purple-600), var(--color-purple-500))",
              color: "#fff",
              boxShadow: "var(--glow-purple-sm)",
            }}
          >
            <Zap className="h-4 w-4" />
            Launch Mission
          </button>
        </div>
      )}

      {/* ── Phase output cards ── */}
      {PHASES.map((meta, i) => (
        <PhaseCard
          key={meta.id}
          meta={meta}
          state={phaseStates[meta.id]}
          phaseNumber={i + 1}
          onToggle={() => toggleExpand(meta.id)}
        />
      ))}

      {/* ── Mission complete banner ── */}
      {done && (
        <MissionComplete
          onDownload={() => downloadReport(phaseStates, summary.fileName)}
        />
      )}
    </div>
  );
}
