// ─── Helpers ──────────────────────────────────────────────────────────────────

function initialPhaseStates(): Record<PhaseId, PhaseState> {
  return Object.fromEntries(
    PHASE_IDS.map((id) => [id, { status: "idle" as PhaseStatus, output: "", expanded: false }])
  ) as Record<PhaseId, PhaseState>;
}

/**
 * Build a compact context string from all completed phases before `upToIndex`.
 * Truncates each phase snippet so the total stays under ~1500 chars.
 */
function buildContext(states: Record<PhaseId, PhaseState>, upToIndex: number): string {
  const parts: string[] = [];
  const perPhase = Math.floor(1400 / Math.max(upToIndex, 1));
  for (let i = 0; i < upToIndex; i++) {
    const phase = PHASES[i];
    const out = states[phase.id]?.output ?? "";
    if (!out) continue;
    const snippet = out.slice(0, perPhase).replace(/\n+/g, " ").trim();
    parts.push(`[${phase.label}]: ${snippet}${out.length > perPhase ? "…" : ""}`);
  }
  return parts.join("\n");
}

function buildMarkdownReport(states: Record<PhaseId, PhaseState>, fileName: string): string {
  const sections = PHASES
    .map((p, i) => {
      const { status, output } = states[p.id];
      if (status !== "done" || !output) return null;
      return `## Phase ${i + 1}: ${p.label}\n\n${output}`;
    })
    .filter(Boolean)
    .join("\n\n---\n\n");

  return [
    `# Autonomous Data Science Analysis`,
    ``,
    `**Dataset:** ${fileName}`,
    `**Generated by:** GadaaLabs Autonomous Data Science Agent`,
    `**Date:** ${new Date().toISOString().slice(0, 10)}`,
    ``,
    `---`,
    ``,
    sections,
  ].join("\n");
}

function downloadReport(states: Record<PhaseId, PhaseState>, fileName: string) {
  const md = buildMarkdownReport(states, fileName);
  const blob = new Blob([md], { type: "text/markdown" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `${fileName.replace(/\.[^.]+$/, "")}_data_science_analysis.md`;
  a.click();
  URL.revokeObjectURL(url);
}

